package projecto;

/**Classe para objectos do tipo Coordenada
 *
 * @author: Pedro Silva - 2007183130
 */
public class Coordenada {
    private int x;
    private int y;
    
    public Coordenada(int x,int y){
        this.x=x;
        this.y=y;
    }
    
    public int getX(){
        return x;
    }
    
    public int getY(){
        return y;
    }
    
}
